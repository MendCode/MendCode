export type HomeLogoFont = "classic" | "mendcode" | "opencode" | "small" | "standard" | "shadow"

const FONT_SETS: Record<HomeLogoFont, Record<string, string[]>> = {
  classic: CLASSIC_FONT,
  mendcode: OPENCODE_FONT,
  opencode: OPENCODE_FONT,
  small: SMALL_FONT,
  standard: STANDARD_FONT,
  shadow: SHADOW_FONT,
}

const FONT_HEIGHTS: Record<HomeLogoFont, number> = {
  classic: 3,
  mendcode: 3,
  opencode: 3,
  small: 5,
  standard: 6,
  shadow: 6,
}

const FONT_MAX_CHARS: Record<HomeLogoFont, number> = {
  classic: 18,
  mendcode: 18,
  opencode: 18,
  small: 14,
  standard: 12,
  shadow: 12,
}

const FONT_GAPS: Record<HomeLogoFont, string> = {
  classic: " ",
  mendcode: " ",
  opencode: " ",
  small: " ",
  standard: " ",
  shadow: "",
}

export function renderAsciiText(value: string, font: HomeLogoFont = "classic") {
  const normalized = (value.trim() || "MendCode").toUpperCase().slice(0, FONT_MAX_CHARS[font])
  const glyphSet = FONT_SETS[font]
  const rowCount = FONT_HEIGHTS[font]
  const letterGap = FONT_GAPS[font]
  const rows = Array.from({ length: rowCount }, () => [] as string[])
  for (const char of normalized) {
    const glyph = glyphSet[char] ?? glyphSet["?"]
    const width = glyph.reduce((max, line) => Math.max(max, line.length), 0)
    for (let i = 0; i < rowCount; i++) rows[i].push(glyph[i] ?? " ")
    for (let i = 0; i < rowCount; i++) rows[i][rows[i].length - 1] = rows[i][rows[i].length - 1].padEnd(width, " ")
  }
  return rows.map((row) => row.join(letterGap)).join("\n")
}

export function asciiTextWidth(value: string, font: HomeLogoFont = "classic") {
  return renderAsciiText(value, font)
    .split("\n")
    .reduce((max, line) => Math.max(max, line.length), 0)
}
